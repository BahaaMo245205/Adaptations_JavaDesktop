    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            String strId = (jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0)).toString();
            rs = c.stat.executeQuery("SELECT * FROM EMPLOYEE where EMID='"+ strId +"'");
            if (rs.next()) {
                EmID.setText(rs.getString("EMID"));
                EmName.setText(rs.getString("EMNAME"));
                EmPhone.setText(rs.getString("EMPHONE"));
                EmAge.setText(rs.getString("AGE"));
                type_working.setSelectedItem(rs.getString("TYPEEMPLOYEE"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jTable1MouseClicked

