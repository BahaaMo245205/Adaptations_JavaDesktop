    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            String Choice = ComSearch.getSelectedItem().toString();
            
            if (Choice.equals("All")){
                rs = c.stat.executeQuery("SELECT * FROM EMPLOYEE");
            }
            else if (Choice == "ID"){
                rs = c.stat.executeQuery("SELECT * FROM EMPLOYEE where EMID='"+TextSearch.getText()+"'");
            }
            else if (Choice == "Name"){
                rs = c.stat.executeQuery("SELECT * FROM EMPLOYEE where EMNAME='" +TextSearch.getText()+"'");
            }
            else if (Choice == "Phone"){
                rs = c.stat.executeQuery("SELECT * FROM EMPLOYEE where EMPHONE='" +TextSearch.getText() + "'");
            }
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

