    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //================(Search)================================
        try {
            String choice = ComSearch.getSelectedItem().toString();
            if (RadoSalary.isSelected()){
                if (choice.equals("All")) {
                    rs = c.stat.executeQuery("SELECT * FROM SALARYES");
                }else if(choice.equals("Name")){
                    rs = c.stat.executeQuery("SELECT * FROM SALARYES WHERE NAME='"+TextSearch.getText().toString()+"'");
                }else if(choice.equals("type work")){  
                    rs = c.stat.executeQuery("SELECT * FROM SALARYES WHERE TYPEWORK='"+TextSearch.getText().toString()+"'");
                }else if (choice.equals("Employee ID")){
                    rs = c.stat.executeQuery("SELECT * FROM SALARYES WHERE EMID='"+TextSearch.getText().toString()+"'");
                }else if (choice.equals("Date (salaries)")){
                    rs = c.stat.executeQuery("SELECT * FROM SALARYES WHERE SDATE='"+TextSearch.getText().toString()+"'");
                }
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));

            }else if (RadoEmoloyee.isSelected()){
                if (choice.equals("All")) {
                    rs = c.stat.executeQuery("SELECT * FROM EMPLOYEE");
                }if (choice.equals("Name")){
                    rs = c.stat.executeQuery("SELECT * FROM EMPLOYEE WHERE EMNAME='"+TextSearch.getText().toString()+"'");
                }if(choice.equals("type work")){
                    rs = c.stat.executeQuery("SELECT * FROM EMPLOYEE WHERE TYPEEMPLOYEE='"+TextSearch.getText().toString()+"'");
                }else if (choice.equals("Employee ID")){
                    rs = c.stat.executeQuery("SELECT * FROM EMPLOYEE WHERE EMID='"+TextSearch.getText().toString()+"'");
                }
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            
            }               
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        //================(Search)================================

    }//GEN-LAST:event_jButton3ActionPerformed

