    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        try {
            String strId = (jTable1.getModel().getValueAt(jTable1.getSelectedRow(),0)).toString();
            if (RadoEmoloyee.isSelected()){
                rs = c.stat.executeQuery("SELECT * FROM EMPLOYEE where EMID='"+ strId +"'");
                if (rs.next()){
                    employee_id.setText(rs.getString("EMID"));
                    salname.setText(rs.getString("EMNAME"));
                    salAge.setText(rs.getString("AGE"));
                    type_working.setSelectedItem(rs.getString("TYPEEMPLOYEE"));
                }
                
            }else if(RadoSalary.isSelected()){
                rs = c.stat.executeQuery("SELECT * FROM SALARYES where ID='"+ strId +"'");
                if (rs.next()){
                    salID.setText(rs.getString("ID"));
                    employee_id.setText(rs.getString("EMID"));
                    Salaries.setText(rs.getString("SALARYE"));
                    rate.setText(rs.getString("RATE"));
                    target.setText(rs.getString("TARGET"));
                    salname.setText(rs.getString("NAME"));
                    salAge.setText(rs.getString("AGE"));
                    type_working.setSelectedItem(rs.getString("TYPEWORK"));
                    datesal.setText(rs.getString("SDATE"));
                    Totile.setText(rs.getString("TOTAL"));

                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    

    }//GEN-LAST:event_jTable1MouseClicked

