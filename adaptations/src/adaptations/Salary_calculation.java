    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        double total = 0;
        
        //=================(rate)========================
        double rates = Double.parseDouble(rate.getText()); 
        if (rates >= 7.0) {
            total += 2000 ;
        }else if (rates >= 5.0){
            total += 1200;
        }else {
            total += 0;
        }
        //===============================================
        //=================(target)========================
        double targets = Double.parseDouble(target.getText().toString());
        if (targets >= 75) {
            total += 2000;
        }else if (targets >= 50){
            total += 1500;
        }else if (targets >= 25){
            total += 1000;
        }else {
            total += 0;
        }
        //===============================================
        //=================(total)========================
        double salaryes = Double.parseDouble(Salaries.getText().toString());
        total += salaryes ;
        Totile.setText(String.valueOf(total));
        //===============================================
    }//GEN-LAST:event_jButton7ActionPerformed

